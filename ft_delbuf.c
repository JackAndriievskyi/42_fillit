#include "fillit.h"

void	ft_delbuf(t_figure ***fig)
{
	size_t	i;

	if (*fig)
	{
		i = 0;
		while ((*fig)[i] != NULL)
			free((*fig)[i++]);
		free(*fig);
		*fig = NULL;
	}
}
